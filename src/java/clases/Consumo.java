package clases;

/**
 *
 * @author Luis
 */
public class Consumo {
    
    private String numero,nombre,consumo,fecha;

    public Consumo() {
    }

    public Consumo(String numero, String nombre, String consumo, String fecha) {
        this.numero = numero;
        this.nombre = nombre;
        this.consumo = consumo;
        this.fecha = fecha;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getConsumo() {
        return consumo;
    }

    public void setConsumo(String consumo) {
        this.consumo = consumo;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }
    
    
    
    
    
    
    
}
